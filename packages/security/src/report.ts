/**
 * Sentinel — Multi-format report generation.
 * Produces professional pentest reports in Markdown, SARIF 2.1.0, JSON, and CSV
 * from a completed or in-progress ScanJob.
 */

import type { Finding, ScanJob, SeverityLevel } from "./types.js";

// ─────────────────────────────────────────────────────────────────────────────
// Shared helpers
// ─────────────────────────────────────────────────────────────────────────────

const SEVERITY_ORDER: SeverityLevel[] = [
	"critical",
	"high",
	"medium",
	"low",
	"info",
];

function capitalise(s: string): string {
	return s.charAt(0).toUpperCase() + s.slice(1);
}

function severityCounts(findings: Finding[]): Record<SeverityLevel, number> {
	const counts: Record<SeverityLevel, number> = {
		critical: 0,
		high: 0,
		medium: 0,
		low: 0,
		info: 0,
	};
	for (const f of findings) {
		counts[f.severity] = (counts[f.severity] ?? 0) + 1;
	}
	return counts;
}

function activeFindings(job: ScanJob): Finding[] {
	return job.findings.filter((f) => !f.falsePositive);
}

