/**
 * Generates a professional penetration-test-style Markdown report with:
 * executive summary, risk heatmap, attack surface overview, and prioritised findings.
 */
export async function generateMarkdownReport(job: ScanJob): Promise<string> {
	const findings = activeFindings(job);
	const counts = severityCounts(findings);
	const date = new Date().toISOString().slice(0, 10);
	const duration =
		job.completedAt && job.startedAt
			? formatDuration(job.completedAt.getTime() - job.startedAt.getTime())
			: "ongoing";

	const lines: string[] = [
		`# Sentinel Security Assessment Report`,
		``,
		`> Generated by setra.sh Sentinel intelligence engine`,
		``,
		`| Field | Value |`,
		`|-------|-------|`,
		`| **Job ID** | \`${job.id}\` |`,
		`| **Target** | \`${job.target}\` |`,
		`| **Status** | ${capitalise(job.status)} |`,
		`| **Date** | ${date} |`,
		`| **Duration** | ${duration} |`,
		`| **Methodology** | ${job.surface ? "Network + Web" : "Static Analysis"} |`,
		``,
		`---`,
		``,
		`## Executive Summary`,
		``,
		`This report documents the findings of an automated security assessment conducted`,
		`by setra's Sentinel intelligence engine against \`${job.target}\`.`,
		``,
		`The assessment identified **${findings.length}** actionable security issues.`,
		counts.critical > 0
			? `**${counts.critical} critical-severity finding(s) require immediate attention** — these represent direct paths to compromise.`
			: `No critical-severity findings were identified during this assessment.`,
		``,
		`### Risk Distribution Heatmap`,
		``,
		`\`\`\``,
		buildHeatmap(counts),
		`\`\`\``,
		``,
		`| Severity | Count | Risk Level |`,
		`|----------|-------|------------|`,
		`| 🔴 Critical | ${counts.critical} | Immediate remediation required |`,
		`| 🟠 High     | ${counts.high}     | Address within 7 days          |`,
		`| 🟡 Medium   | ${counts.medium}   | Address within 30 days         |`,
		`| 🔵 Low      | ${counts.low}      | Address in next sprint         |`,
		`| ⚪ Info      | ${counts.info}     | Informational — review at will |`,
		``,
	];

	// Attack surface overview
	if (job.surface) {
		lines.push(
			`## Attack Surface Overview`,
			``,
			`| Attribute | Value |`,
			`|-----------|-------|`,
			`| **Hostname** | ${job.surface.hostname ?? "—"} |`,
			`| **IP Address** | ${job.surface.ipAddress ?? "—"} |`,
			`| **Open Ports** | ${job.surface.openPorts.filter((p) => p.state === "open").length} |`,
			`| **Services** | ${job.surface.services.length} |`,
			`| **Web Endpoints** | ${job.surface.webEndpoints.length} |`,
			`| **Technologies** | ${job.surface.technologies.join(", ") || "—"} |`,
			``,
		);

		if (job.surface.openPorts.length > 0) {
			lines.push(
				`### Open Ports`,
				``,
				`| Port | Protocol | Service | Version |`,
				`|------|----------|---------|---------|`,
				...job.surface.openPorts
					.filter((p) => p.state === "open")
					.map(
						(p) =>
							`| ${p.port} | ${p.protocol} | ${p.service ?? "—"} | ${p.version ?? "—"} |`,
					),
				``,
			);
		}
	}

	// Findings by severity
	lines.push(`## Findings`, ``);

	for (const sev of SEVERITY_ORDER) {
		const group = findings.filter((f) => f.severity === sev);
		if (!group.length) continue;

		const icon = {
			critical: "🔴",
			high: "🟠",
			medium: "🟡",
			low: "🔵",
			info: "⚪",
		}[sev];
		lines.push(`### ${icon} ${capitalise(sev)} Severity (${group.length})`, ``);

		for (const f of group) {
			lines.push(
				`#### ${f.title}`,
				``,
				`| Field | Value |`,
				`|-------|-------|`,
				`| **Tool** | \`${f.toolUsed}\` |`,
				`| **Target** | \`${f.target}\` |`,
				`| **Discovered** | ${f.discoveredAt.toISOString().slice(0, 19).replace("T", " ")} |`,
				...(f.cvss ? [`| **CVSS Score** | ${f.cvss.baseScore}/10 |`] : []),
				...(f.cveIds.length ? [`| **CVEs** | ${f.cveIds.join(", ")} |`] : []),
				...(f.mitreAttackIds.length
					? [`| **MITRE ATT&CK** | ${f.mitreAttackIds.join(", ")} |`]
					: []),
				``,
				`**Description**`,
				``,
				f.description,
				``,
				`**Evidence**`,
				``,
				"```",
				f.evidence.slice(0, 600),
				"```",
				``,
				`**Remediation**`,
				``,
				f.remediation,
			);

			if (f.references.length) {
				lines.push(``, `**References**`, ``);
				for (const ref of f.references) lines.push(`- ${ref}`);
			}

			lines.push(``, `---`, ``);
		}
	}

	// Remediation priority matrix
	lines.push(
		`## Remediation Priority Matrix`,
		``,
		`| Priority | Finding | Effort | Impact |`,
		`|----------|---------|--------|--------|`,
		...findings
			.filter((f) => ["critical", "high"].includes(f.severity))
			.slice(0, 10)
			.map((f, i) => `| ${i + 1} | ${f.title} | Medium | High |`),
		``,
	);

	// Appendix
	lines.push(
		`## Appendix: Scan Timeline`,
		``,
		`| Timestamp | Phase | Action |`,
		`|-----------|-------|--------|`,
		...job.agentLog
			.slice(0, 30)
			.map(
				(e) =>
					`| ${e.ts.toISOString().slice(11, 19)} | ${e.phase} | ${e.action}: ${e.detail.slice(0, 60)} |`,
			),
		``,
		`*Full log contains ${job.agentLog.length} entries.*`,
		``,
	);

	return lines.join("\n");
}

