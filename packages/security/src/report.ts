/**
 * Exports findings as CSV for spreadsheet import or bulk ticket creation
 * in Jira, GitHub Issues, Linear, and similar trackers.
 */
export async function generateCsv(job: ScanJob): Promise<string> {
	const findings = activeFindings(job);

	const headers = [
		"ID",
		"Title",
		"Severity",
		"CVSS Score",
		"CVEs",
		"MITRE ATT&CK",
		"Target",
		"Tool Used",
		"Description",
		"Remediation",
		"Evidence",
		"Discovered At",
		"References",
	];

	const rows = findings.map((f) => [
		f.id,
		f.title,
		f.severity,
		f.cvss?.baseScore?.toString() ?? "",
		f.cveIds.join("; "),
		f.mitreAttackIds.join("; "),
		f.target,
		f.toolUsed,
		f.description,
		f.remediation,
		f.evidence.slice(0, 300).replace(/\n/g, " "),
		f.discoveredAt.toISOString(),
		f.references.join("; "),
	]);

	const lines = [headers, ...rows].map((row) =>
		row.map((cell) => `"${String(cell).replace(/"/g, '""')}"`).join(","),
	);

	return lines.join("\n");
}

// ─────────────────────────────────────────────────────────────────────────────
// Internal helpers
// ─────────────────────────────────────────────────────────────────────────────

function buildHeatmap(counts: Record<SeverityLevel, number>): string {
	const maxBar = 30;
	const total = Object.values(counts).reduce((a, b) => a + b, 0) || 1;
	const lines: string[] = [];

	for (const sev of SEVERITY_ORDER) {
		const n = counts[sev];
		const bar = "█".repeat(Math.round((n / total) * maxBar));
		const label = capitalise(sev).padEnd(9);
		lines.push(`${label} ${bar.padEnd(maxBar)} ${n}`);
	}

	return lines.join("\n");
}

function sarifLevel(severity: SeverityLevel): string {
	switch (severity) {
		case "critical":
		case "high":
			return "error";
		case "medium":
			return "warning";
		default:
			return "note";
	}
}

function slugify(s: string): string {
	return s
		.toLowerCase()
		.replace(/[^a-z0-9]+/g, "-")
		.slice(0, 50);
}

function dedupHash(f: Finding): string {
	// Deterministic fingerprint without importing crypto for this helper
	const key = `${f.target}|${f.title}|${f.toolUsed}`;
	let h = 0;
	for (let i = 0; i < key.length; i++) {
		h = (Math.imul(31, h) + key.charCodeAt(i)) >>> 0;
	}
	return h.toString(16).padStart(8, "0");
}

function formatDuration(ms: number): string {
	const s = Math.floor(ms / 1000);
	if (s < 60) return `${s}s`;
	const m = Math.floor(s / 60);
	if (m < 60) return `${m}m ${s % 60}s`;
	return `${Math.floor(m / 60)}h ${m % 60}m`;
}
