/**
 * Full structured JSON export — suitable for machine processing, API responses,
 * and integrations that consume Sentinel output programmatically.
 */
export async function generateJson(job: ScanJob): Promise<string> {
	const counts = severityCounts(activeFindings(job));

	const output = {
		jobId: job.id,
		target: job.target,
		status: job.status,
		phase: job.phase,
		startedAt: job.startedAt?.toISOString(),
		completedAt: job.completedAt?.toISOString(),
		summary: {
			totalFindings: job.findings.length,
			activeFindings: activeFindings(job).length,
			falsePositives: job.findings.filter((f) => f.falsePositive).length,
			bySeverity: counts,
		},
		surface: job.surface,
		findings: activeFindings(job).map((f) => ({
			id: f.id,
			title: f.title,
			description: f.description,
			severity: f.severity,
			cvss: f.cvss,
			cveIds: f.cveIds,
			mitreAttackIds: f.mitreAttackIds,
			remediation: f.remediation,
			references: f.references,
			discoveredAt: f.discoveredAt.toISOString(),
			target: f.target,
			toolUsed: f.toolUsed,
			evidence: f.evidence,
		})),
		agentLog: job.agentLog.map((e) => ({
			...e,
			ts: e.ts.toISOString(),
		})),
		generatedAt: new Date().toISOString(),
		generator: "Sentinel 1.0.0 (setra.sh)",
	};

	return JSON.stringify(output, null, 2);
}

