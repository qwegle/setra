/**
 * Serialises findings as SARIF 2.1.0 — compatible with GitHub Code Scanning,
 * VS Code SARIF Viewer, Azure DevOps, and any OASIS-compliant tool.
 */
export async function generateSarif(job: ScanJob): Promise<string> {
	const findings = activeFindings(job);
	const rules = new Map<string, object>();

	const results = findings.map((f) => {
		const ruleId = `sentinel/${f.toolUsed}/${slugify(f.title)}`;
		if (!rules.has(ruleId)) {
			rules.set(ruleId, {
				id: ruleId,
				name: f.title,
				shortDescription: { text: f.description.slice(0, 256) },
				fullDescription: { text: f.description },
				helpUri: f.references[0] ?? "https://setra.sh/sentinel",
				properties: {
					tags: ["security", f.severity, ...f.mitreAttackIds],
				},
			});
		}

		return {
			ruleId,
			level: sarifLevel(f.severity),
			message: {
				text: `${f.description}\n\n**Remediation:** ${f.remediation}`,
			},
			locations: [
				{
					physicalLocation: {
						artifactLocation: { uri: f.target, uriBaseId: "%SRCROOT%" },
					},
					logicalLocations: [{ fullyQualifiedName: f.target }],
				},
			],
			fingerprints: {
				"sentinel/v1": dedupHash(f),
			},
			properties: {
				severity: f.severity,
				cvssScore: f.cvss?.baseScore,
				cveIds: f.cveIds,
				mitreAttackIds: f.mitreAttackIds,
				evidence: f.evidence.slice(0, 200),
				discoveredAt: f.discoveredAt.toISOString(),
			},
		};
	});

	const sarif = {
		$schema:
			"https://raw.githubusercontent.com/oasis-tcs/sarif-spec/master/Schemata/sarif-schema-2.1.0.json",
		version: "2.1.0",
		runs: [
			{
				tool: {
					driver: {
						name: "Sentinel",
						version: "1.0.0",
						semanticVersion: "1.0.0",
						informationUri: "https://setra.sh/sentinel",
						rules: [...rules.values()],
					},
				},
				originalUriBaseIds: {
					"%SRCROOT%": {
						uri: job.target.startsWith("/")
							? `file://${job.target}/`
							: `${job.target}/`,
					},
				},
				results,
				properties: {
					jobId: job.id,
					target: job.target,
					scanStatus: job.status,
					startedAt: job.startedAt?.toISOString(),
					completedAt: job.completedAt?.toISOString(),
				},
			},
		],
	};

	return JSON.stringify(sarif, null, 2);
}

